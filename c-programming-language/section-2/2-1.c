#include <stdio.h>
#include <limits.h>

int main() {
    printf("char: %d\n", CHAR_MAX);
    printf("unsigned char: %d\n", UCHAR_MAX);
    printf("short: %d\n", SHRT_MAX);
    printf("unsigned short: %d\n", USHRT_MAX);
    printf("long: %ld\n", LONG_MAX);
    printf("unsigned long: %lu\n", ULONG_MAX);
}